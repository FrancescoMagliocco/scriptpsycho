function gamedataStatType.Get(statName)
    statName = statName:lower():gsub("%W", "")
    for _, statTable in ipairs(_gamedataStatType) do
        if statTable[1]:lower():gsub("%W", "") == statName then
            return statTable
        end
    end
end

-- Specify index as zero based.
function gamedataStatType.GetStatTableFromIndex(statIndex)
    statIndex = statIndex + 1
    if statIndex >= 1 and statIndex <= #_gamedataStatType then
        return _gamedataStatType[statIndex]
    end
end

function gamedataStatType.GetAll()
    return _gamedataStatType
end

return gamedataStatType
